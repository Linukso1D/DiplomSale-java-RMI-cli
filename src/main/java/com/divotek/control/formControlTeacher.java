private void updateListMark()
{
 Teacher t = Singleton.getInstance().getItTeacher();
        ArrayList templateSubject =  t.getControlSubject();
        Map subjects = Singleton.getInstance().getOnePerson(jList1.getSelectedIndex()).getSubjects();
         DefaultListModel listmodel = new DefaultListModel();
            subjects.forEach((k,v)->{
             if(templateSubject.indexOf(k)!=-1)
                {
                  
                    listmodel.addElement(k+" "+v);
                }
            });
            jList2.setModel(listmodel);
}
