    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String  value = jList2.getSelectedValue();
       
        Map subjects = Singleton.getInstance().getOnePerson(jList1.getSelectedIndex()).getSubjects();
        Map testMap = new LinkedHashMap(getListElement(value));
        testMap.forEach((k,v)->
        {
            if(subjects.containsKey(k))
            {
                subjects.remove(k);
                DefaultListModel model = (DefaultListModel)jList2.getModel();
                model.remove(jList2.getSelectedIndex());
               
            }
        });
        
       
    }//GEN-LAST:event_jButton3ActionPerformed
